#include <fractol.h>

int	ft_verif_num(char *arg)
{
	int	i;

	i = 0;
	if (!arg)
		return (0);
	while (arg[i])
	{
		if (!(arg[i] >= '0' && arg[i] <= '9')
			&& !(arg[i] == '-' && i == 0)
			&& !(arg[i] == '+' && i == 0))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
